    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
         if (!validasiInput()) return;
        try {
            String sql = "INSERT INTO barang VALUES (?,?,?,?,?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtkdbrg.getText().trim());
            ps.setString(2, txtnmbrg.getText().trim());
            ps.setString(3, cbjbrg.getSelectedItem().toString());
            ps.setLong(4, Long.parseLong(txthrgbl.getText().trim()));
            ps.setLong(5, Long.parseLong(txthrgjl.getText().trim()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            tampilData("");
            bersih();
        } catch (SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Kode barang sudah ada, gunakan kode lain!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
        if (txtkdbrg.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data dari tabel terlebih dahulu!");
            return;
        }
        if (!validasiInput()) return;
        try {
            String sql = "UPDATE barang SET nm_brg=?, jenis=?, hargabeli=?, hargajual=? WHERE kd_barang=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtnmbrg.getText().trim());
            ps.setString(2, cbjbrg.getSelectedItem().toString());
            ps.setLong(3, Long.parseLong(txthrgbl.getText().trim()));
            ps.setLong(4, Long.parseLong(txthrgjl.getText().trim()));
            ps.setString(5, txtkdbrg.getText().trim());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            tampilData("");
            bersih();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_bubahActionPerformed

