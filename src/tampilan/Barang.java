    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        if (txtkdbrg.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data dari tabel terlebih dahulu!");
            return;
        }
        int konfirm = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin hapus data barang: " + txtnmbrg.getText() + "?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION
        );
        if (konfirm == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM barang WHERE kd_barang=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, txtkdbrg.getText().trim());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                tampilData("");
                bersih();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_bhapusActionPerformed

